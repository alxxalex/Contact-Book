    public void setContact(Contact c) {
        String elem[] = c.getDataNasterii().split("-");

        tfNume.setText(c.getNume());
        tfPrenume.setText(c.getPrenume());
        tfAn.setText(elem[0]);
        tfLuna.setText(elem[1]);
        tfZi.setText(elem[2]);
        tfNrTel.setText(c.getNumarTel().toString());
    }

